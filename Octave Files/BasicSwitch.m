day = input('Enter the day of the week as a number: ')

switch day
	case 1
		disp('It is Monday.')
	case 2
		disp('It is Tuesday.')
	case 3
		disp('It is Wednesday.')
	case 4
		disp('It is Thursday.')
	case 5
		disp('It is Friday.')
	case 6
		disp('It is Saturday.')
	case 7
		disp('It is Sunday.')
	otherwise
		disp('That is an invalid day!')
end
